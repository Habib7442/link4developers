// Helper function to detect platform from URL
export function detectPlatformFromUrl(url: string): string | null {
  const urlLower = url.toLowerCase()

  // Special cases for platforms with multiple domains
  if (urlLower.includes('x.com') || urlLower.includes('twitter.com')) {
    return 'twitter'
  }
  if (urlLower.includes('linkedin.com')) {
    return 'linkedin'
  }
  if (urlLower.includes('instagram.com')) {
    return 'instagram'
  }
  if (urlLower.includes('github.com')) {
    return 'github'
  }
  if (urlLower.includes('youtube.com') || urlLower.includes('youtu.be')) {
    return 'youtube'
  }
  if (urlLower.includes('facebook.com') || urlLower.includes('fb.com')) {
    return 'facebook'
  }
  if (urlLower.includes('tiktok.com')) {
    return 'tiktok'
  }
  if (urlLower.includes('discord.gg') || urlLower.includes('discord.com')) {
    return 'discord'
  }
  if (urlLower.includes('t.me') || urlLower.includes('telegram.org')) {
    return 'telegram'
  }
  if (urlLower.includes('wa.me') || urlLower.includes('whatsapp.com')) {
    return 'whatsapp'
  }
  if (urlLower.includes('reddit.com')) {
    return 'reddit'
  }
  if (urlLower.includes('twitch.tv')) {
    return 'twitch'
  }
  if (urlLower.includes('snapchat.com')) {
    return 'snapchat'
  }

  // Fallback: check if URL contains platform name
  for (const [platform, config] of Object.entries(SOCIAL_PLATFORMS)) {
    if (config.baseUrl && urlLower.includes(platform)) {
      return platform
    }
  }

  return null
}

// Helper function to get platform color
export function getPlatformColor(platform: string): string {
  return SOCIAL_PLATFORMS[platform]?.color || '#666666'
}

// Helper function to validate custom icon URL
export function isValidIconUrl(url: string): boolean {
  try {
    const urlObj = new URL(url)
    const validExtensions = ['.png', '.jpg', '.jpeg', '.svg', '.webp', '.gif']
    const pathname = urlObj.pathname.toLowerCase()
    
    return validExtensions.some(ext => pathname.endsWith(ext))
  } catch {
    return false
  }
}
